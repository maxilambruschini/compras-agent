def main() -> None:
    """Parse CLI flags and dispatch to the appropriate command."""
    parser = argparse.ArgumentParser(
        description=(
            "Calibration script for the Argentine invoice extraction pipeline (D-11). "
            "Generates ground truth via Claude Opus 4.7 and diffs against GPT-4o extraction."
        )
    )
    parser.add_argument(
        "--generate-ground-truth",
        action="store_true",
        help=(
            "Generate {fixture}_ground_truth.json files using Claude Opus 4.7. "
            "Requires ANTHROPIC_API_KEY."
        ),
    )
    parser.add_argument(
        "--fixture",
        type=str,
        default=None,
        help=(
            "Filter to fixtures whose stem starts with this prefix "
            "(e.g. 'factura_a', 'remito'). Operates on all fixtures if omitted."
        ),
    )
    parser.add_argument(
        "--report-only",
        action="store_true",
        help=(
            "Run the diff loop using existing ground truth files (no Anthropic call). "
            "Equivalent to running without any flag — this flag reserves the hook for "
            "future use."
        ),
    )
    args = parser.parse_args()

    if args.generate_ground_truth:
        sys.exit(cmd_generate_ground_truth(args))
    else:
        sys.exit(cmd_diff(args))


if __name__ == "__main__":
    main()
